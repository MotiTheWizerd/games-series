export const PIECE_KINDS: ReadonlyArray<PieceKind> = ['I', 'O', 'T', 'S', 'Z', 'J', 'L']

export function getShape(kind: PieceKind, rotation: number): Shape {
  const variants = PIECE_SHAPES[kind]
  return variants[rotation % variants.length]
}

export function randomPiece(): PieceKind {
  return PIECE_KINDS[Math.floor(Math.random() * PIECE_KINDS.length)]
}
